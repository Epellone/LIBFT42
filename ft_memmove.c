#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*tmp;
	char	*dest;

	tmp = (char *)src;
	dest = (char *)dst;
	if (tmp < dest)
	{
		while (len--)
			dest[len] = tmp[len];
	}
	else
		ft_memcpy(dest, tmp, len);
	return (dst);
}
